def my_func(x, y):
    res = x ** y
    return res

def second_func(x,y):
    result = x
    while y<-1:
        if y==0:
            break
        else:
            result*=x
            y+=1

    finish_res=1/result
    return finish_res

print(my_func(4,-2))
print(second_func(4,-2))








